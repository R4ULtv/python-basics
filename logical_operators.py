# LOGICAL OPERATORS 

y = 1
x = 1

if y == x and x == 1:
    # {Logical AND: True if both the operands are true}
    print("Example of AND")

if y == x or x == 1:
    # {Logical OR: True if either of the operands is true}
    print("Example of OR")

if not y:
    # {Logical NOT: True if operand is false}
    print("Example of NOT")